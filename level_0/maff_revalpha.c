#include <unistd.h>

int main()
{
    char z;
    char Y;
    z = 'z';
    Y = 'Y';
    while (z >='a' && Y >= 'A')
    {
        write(1,&z,1);
        write(1,&Y,1);
        z-=2;
        Y-=2;
    }
    write(1,"\n",1);
    return(0);
}